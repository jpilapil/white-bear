$("#letsGo").click(function () {
   var isValidEmail = false;
   var isValidPassword = false;

   //email first card
   var emailInput = $("#inputEmail1").val();
   var emailSplit = emailInput.split("@");
   let uniqueCharEmail = new Set(emailSplit[0]);
   let uniqueCharEmailArr = [...uniqueCharEmail];
   $("#inputEmail1").addClass("is-invalid"); // adds invalid to input field by default
   if (emailInput == "") {
      $(".invalidEmailFeedback").text("Please enter your email address");
   } else if (uniqueCharEmailArr.length < 3) {
      $(".invalidEmailFeedback").text(
         "Email must contain at least 3 unique characters"
      );
   } else {
      $("#inputEmail1").removeClass("is-invalid");
      $("#inputEmail1").addClass("is-valid");
      $(".invalidEmailFeedback").hide();
      isValidEmail = true;
   }

   // password first card
   var passwordInput = $("#inputPassword1").val();
   $("#inputPassword1").addClass("is-invalid"); // adds invalid to input field by default
   if (passwordInput.length === 0) {
      $(".invalidPasswordFeedback").text("Please enter your password");
   } else if (passwordInput.length < 9) {
      $(".invalidPasswordFeedback").text(
         "Password must be at least 9 characters"
      );
   } else if (
      passwordInput == emailSplit[0] // before the @
   ) {
      $(".invalidPasswordFeedback").text(
         "Email cannot be the same as password"
      );
   } else if (commonPasswordsEightChar.includes(passwordInput)) {
      // checks if password input is in the list of common passwords
      $(".invalidPasswordFeedback").text("Password is not strong enough");
   } else {
      $("#inputPassword1").removeClass("is-invalid");
      $("#inputPassword1").addClass("is-valid");
      $(".invalidPasswordFeedback").hide();
      isValidPassword = true;
   }

   // PASSWORD ENCRYPTION

   // fromCharCode - https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/String/fromCharCode
   // charCodeAt - https://developer.mozilla.org/en-US/docs/Web/JavaScript/Reference/Global_Objects/String/charCodeAt

   // function to increment characters + 1
   // 'Z' will wrap back around to 'A', 9 to 0
   function incrementPasswordChar(passwordInput) {
      if (passwordInput.includes("z")) {
         return "a";
      } else if (passwordInput.includes("Z")) {
         return "A";
      } else if (passwordInput.includes(9)) {
         return 0;
      }
      return String.fromCharCode(passwordInput.charCodeAt(0) + 1);
   }

   // split password input for encryption
   let splitPassword = passwordInput
      .split("") // split the password into individual characters (makes it an array as well)
      .map(function (char) {
         // map through each characters
         return incrementPasswordChar(char); // runs each character through nextChar function which increases the value of the given character by 1. (a = b, m = n, etc)
      })
      .join(""); // joins the characters back together

   // console.log(splitPassword);

   // USER OBJECT
   var user = {
      _id: "",
      email: emailInput,
      password: splitPassword, // encrypted password
      createdOn: "",
   };

   // GETS CREATEDON VALUE

   // creates padding for the dates and adds a 0 if they are one digit

   var todaysDate = new Date(); // generate the current date
   // takes the current date
   // gets respective properties from the current date
   var todaysYear = todaysDate.getYear() - 100;
   var todaysMonth = todaysDate.getMonth() + 1; // months are 0 index. +1 to show current month
   var todaysDay = todaysDate.getDate();
   var todaysHour = todaysDate.getHours();
   var todaysMin = todaysDate.getMinutes();
   var todaysSec = todaysDate.getSeconds();
   var todaysMilli = todaysDate.getMilliseconds();
   var todaysDateTime = // concatenates all of the properties we need for todays date and time
      "" + // converts it to a string
      addPadding(todaysYear) +
      addPadding(todaysMonth) +
      addPadding(todaysDay) +
      addPadding(todaysHour) +
      addPadding(todaysMin) +
      addPadding(todaysSec);
   user.createdOn = Number(todaysDateTime); // assigns the string of todaysDateTiome to the user object's createdOn property

   // _id VALUE

   var randomNumber = Math.floor(Math.random() * 1000);
   // console.log(todaysMilli);
   // console.log(idPadding(todaysMilli));

   // console.log(randomNumber);
   // console.log(idPadding(randomNumber));

   var paddedMilli = idPadding(todaysMilli);
   var paddedRandom = idPadding(randomNumber);

   var idNumber = paddedMilli + paddedRandom;
   user._id = idNumber; // assigns the string of idNumber to the user object's _id property

   // if all requirements in email and password inputs are correct, log the user object to the console.
   if (isValidEmail === true && isValidPassword === true) {
      console.log(user);
   }
});

